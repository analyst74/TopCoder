class BlackAndWhiteSolitaire {
public:
	int minimumTurns(string cardFront) {
		int cb = 0;
		int cw = 0;
		char cur = 'B';
		for (int i=0;i<cardFront.length();i++)
		{
			if (cardFront[i] == cur)
				cb++;
			else
				cw++;
				
			if (cur == 'B')
				cur = 'W';
			else
				cur = 'B';
		}
		
		return min(cb,cw);
	}
};


